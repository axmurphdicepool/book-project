  "Prh-classics" AS source
  ,Title AS title
  ,Author AS author
  ,null AS review
  ,null AS reviews_count
  ,Year AS pblished_year
  ,null AS price_eur
  ,"" AS length
  ,null AS awards
  ,null AS bestseller
  ,TRUE AS classic
  ,"" AS publisher
  ,"" AS book_series
  ,"" AS genre
  ,null AS years_on_bestsellers_list
FROM `book-project-479914.cleaned_data.classics_list-v2` 


